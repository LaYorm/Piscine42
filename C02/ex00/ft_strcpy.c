char	*ft_strcpy(char *dest, char *src)
{
	int	compt;

	compt = 0;
	while (src[compt] != '\0')
	{
		dest[compt] = src[compt];
		compt++;
	}
	dest[compt] = '\0';
	return (dest);
}
